#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;
	int	i;

	n = 1;
	while (n < ac)
	{
		i = 0;
		while (av[n][i])
		{
			write(1, &av[n][i], 1);
			i++;
		}
		n++;
		write(1, "\n", 1);
	}
	return (0);
}
